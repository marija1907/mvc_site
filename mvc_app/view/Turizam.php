 <div class="par_turizam">
 <p><strong>Turistička ponuda Kuršumlije</strong> je u znaku tri banje, Prolom vode, Đavolje Varoši, 
 brojih darova prirode i bogate prošlosti. Sa jedne strane jedinstveno neolitsko naselje Pločnik 
 na arheološkom lokalitetu Vinčanske kulture, sa druge jedan od najneobičnijih fenomena prirode u svetu
 Đavolja Varoš. Sa jedne strane Prolom Banja, sa druge Lukovska Banja, i u sredini Kuršumlijska banja
 sa najslavnijom prošlošću. A u cetralnom jezgru Kuršumlija, prva prestonica rodonačelnika dinastije Nemanjić. 
 Stefan Nemanja je ovde podigao svoje prve zadužbine – Manastir Svetog Nikole i Manastir Presvete Bogorodice.
 Opština je poznata po tome što se u njoj začela srpska državnost, kultura i duhovnost.</p>
 <p>Jedno od posebno zanimljivih mesta je jedan od najneobičnijih fenomena prirode u svetu 
 <strong>„Đavolja Varoš“.</strong> Spomenik prirode „Đavolja Varoš“ nalazi se na jugu Srbije, 
 27 km jugoistočno od Kuršumlije, a 89 km jugozapadno od Niša. Od Beograda je udaljen 288 km.</p>
 <p>Ovaj spomenik prirode čine dva, u svetu retka, prirodna fenomena: zemljane figure, kao specifični oblici
 reljefa koji u prostoru deluju vrlo atraktivno, i dva izvora jako kisele vode sa visokom mineralizacijom.
 Svetski poznato čudo prirode Đavolju Varoš karakterišu figure koje je priroda erozijom stvorila od kamena 
 i zemlje, ali karakteristične su i brojne legende. </p>
 <p>Okolina Kuršumlije je poznata i po lekovitim vodama i banjskom turizmu.
 Pod obroncima Radan planine nalazi se <strong>Prolom Banja</strong> koja se već decenijama ponosi lekovitom
 mineralnom Prolom vodom i brojnim banjskim tretmanima. Poseban doživljaj je „Etno kuća“ koja predstavlja postavku
 seoskog domaćinstva sa početka 20. veka.</p>
 <p>Do <strong>Lukovske Banje</strong> možete doći iz pravca Kuršumlije ili iz pravca Brusa i Kopaonika. Ovo je najviša banja u 
 Srbiji, koja pruža brojne mogućnosti za aktivan odmor i rekreaciju. Lukovska Banja je poznata po velikom broju
 izvora mineralne vode, savremenim banjskim tretmanima, stazom za skijanje sa žičarom i šest obeleženih staza za
 šetnju u prirodnom ambijentu.</p>
