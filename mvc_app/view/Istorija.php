    <p>Grad Kuršumlija leži na temeljima rimskih građevina. Prilikom izgradnje industrijskih, stambenih, 
       školskih, komunalnih i drugih objekata otkriveni su tragovi rimskih zidina. Pronađeni su, pored 
       ostalog, ostaci rimskog vodovoda, miljokaz i novac rimskih careva Lucija Domicija Aurelijana
       (oko 215-275 godine) i Epidamnosa.</p>
    <p>Naselje na ušću Banjske u Toplicu razvilo se u doba Rimljana, najverovatnije u drugom veku.
       Nastalo je u blizini kastela, vojničkog utvrđenja, na trgovačkom putu Niš – Lješ. “Rimski car
       Aleksandar Sever dao je vojnicima zemlju i dozvolio im da stanuju van logora i da se žene.
       Tada niču njihova naselja“ (Sima Marinković, “Toplica naš kraj“, str. 51.). Rimski naziv naselja
       bio je Ad Fines (granica, krajnja stanica, krajište), što znači na kraju ondašnjih provincija 
       Dalmacije i Mezije.</p>
    <p>Prilikom izgradnje kupatila u Kuršumlijskoj Banji pronađene su opeke i zidine drevnih građevina,
       zatim srebrni novac imperatora Filipa (241-249), rimski nadgrobni spomenik i drugi predmeti iz tih
       vremena, kada su ne samo legionari i vojnici vidali rane, već su ovo prirodno lečilište koristili
       i državnici tog vremena za lečenje, oporavak i odmor.</p>
    
