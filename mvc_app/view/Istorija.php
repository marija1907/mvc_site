    	<p>Toplički ustanak 1917. godine je bio srpski ustanak protiv bugarske i austrougarske okupacije 
           za vreme Prvog svetskog rata. To je jedini „ustanak iza frontova“, odnosno jedini ustanak u nekoj
           državi okupiranoj od Centralnih sila tokom celog Prvog svetskog rata.</p>
        <p>Trajao je od 21. februara do 25. marta 1917. godine. Ustanička vojska od petnaestak hiljada boraca 
           sručila se u rano proleće sa planina u ravnice i zarobila oko hiljadu bugarskih vojnika i oslobodila
           veliki deo južne Srbije. Najveće borbe su vođene u dolini južne Morave, na Kopaoniku i na severu 
           Kosova.</p>
        <p>Ofanzivom centralnih sila i strahovitom odmazdom u proleće 1917. godine „Toplička država“ je slomljena.
           U samom okrugu do temelja je uništeno pedeset pet sela. U jesen iste godine Toplički ustanak je konačno
           ugušen u krvi. Okupatorski vojnici, najviše bugarski, ubijali su ne samo poslednje učesnike pobune,
           nego i žene, decu, starce.</p>
    
