<p class="paragraf.sidebar">Prve Nemanjine zadužbine bile su centri duhovnog i kulturnog života sve do potpunog pada srpske
   srednjovekovne države pod otomansku vlast. Tada je počelo rušenje i manastiri su zapusteli. Kada su
   ovi krajevi oslobođeni od otomanskog ropstva, jedva su pronašli temelje Sv. Bogorodice zarasle u šikaru,
   a manastir Sv. Nikola bio je u jadnom stanju.
   Prva zadužbina Stefana Nemanje obnovljena je u decenijama posle oslobođenja. Novi duhovni život počeo je 
   2013. godine, kada je manastir Sv. Nikola posle 323 godine dobio prvog monaha. Manastir Sv. Bogorodica čeka
   obnovu i pažnju kakvu samo zaslužuje nemi svedok velikih državničkih i ktitorskih početaka rodonačelnika
   srpske srednjovekovne države – Stefana Nemanje.</p>
