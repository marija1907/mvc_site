<h1>Kuršumlija - Prestonica Nemanjića</h1>
 
    <p>Kuršumlija je u Srednjem veku bila prva prestonica rodonačelnika dinastije Nemanjić - Stefana Nemanje
     u vreme dok je bio udeoni knez i ovde je podigao svoje prve zadužbine šezdesetih godina 12. veka - Između 1159.
     I 1168. godine. On je ovde sagradio svoja prva dva manastira Sv. Nikolu i Sv. Bogorodicu, i pokrio ih olovom 
     koje je davalo blještav odsjaj, pa je mesto nazvano Bele Crkve.</p>
     <p>
     Kuršumlija se nalazi u središnjem delu južne Srbije i teritorijalno pripada Topličkom okrugu. Prostire se na 
     površini od 952 kilometra kvadratna. Smeštena je na obodu Kuršumlijske kotline i okružena brojnim uzvišenjima.
     Zahvata gornji sliv reke Toplice i njenih pritoka, Kosanice i Banjske, i prostire se na jugoistočnim padinama
     Kopaonika i severozapadnim obroncima planine Radan.
     </p>
     <p>Dolinom reke Toplice i Kosanice prolazi važan magistralni i železnički pravac Niš – Prokuplje – Kuršumlija
      – Kosovo Polje. Preko Blaca, Kuršumlija je putnim pravcem povezana sa Brusom, Kopaonikom, Kruševcem i Beogradom.
     </p>
     <p>Centar Opštine, gradsko naselje Kuršumlija, nalazi se na 386 m nadmorske visine i leži na tri reke – 
     Toplici, Banjskoj i Kosanici. Na teritoriji opštine Kuršumlija postoje tri banje – Lukovska, Kuršumlijska i 
     Prolom, kao i svetska atrakcija i prirodno dobro od izuzetnog značaja – Đavolja Varoš. Kuršumlija se nalazi
     na strategijski važnoj saobraćajnici Niš – Priština – Peć. Od Beograda je udaljena 296, Leskovca 73, Kruševca 
     71 i Prištine 61 km. Reljef je brdsko planinski, a klima umereno kontinentalna.
     </p>
  
